public class 대입산술연산자 {

	public static void main(String[] args) 
	{
		int num1;
		int num2;
		
		
		num1=714;
		num2=500;
		
		System.out.println(num1 + "+" + num2 + "=" + (num1 + num2));
		System.out.println(num1 + "-" + num2 + "=" + (num1 - num2));
		System.out.println(num1 + "*" + num2 + "=" + (num1 * num2));
		System.out.println(num1 + "/" + num2 + "=" + (num1 / num2));
		System.out.println(num1+ "%" + num2 + "=" + (num1 % num2));
	}

}
